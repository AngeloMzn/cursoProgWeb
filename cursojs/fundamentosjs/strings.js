const escola = 'Cod3r'

console.log(escola.charAt(4)) // obter determinado caractere dentro da sting

console.log(escola.charAt(5)) // js não acusa erro mas não retorna nada

console.log(escola.charCodeAt(3)) // obter codigo do caractere

console.log(escola.indexOf('3')) // obter caractere do indice

console.log(escola.substring(1)) // caracteres a partir do indice

console.log(escola.substring(0, 3))// 3 caracteres apartir do indice 0

console.log('escola '.concat(escola).concat('!')) // concatenação

console.log('escola ' + escola + '!') // concatena da mesma forma

console.log(escola.replace(3, 'e')) // mudar o caractere do indice

console.log('Ana,Maria,Pedro'.split(',')) // criação de array separando a string



